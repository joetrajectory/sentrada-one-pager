"""
Sentrada crossword layout engine.

Generates a British-style crossword from a company's answer/clue candidates and
renders a print-ready A2 puzzle page onto a blank cream template, with real,
perfect text every time.

    python crossword.py --template "Blank crossword template 25mb - upscaled.jpg" \
        --data test_cognism.json --output cognism_crossword.png

Sibling of the newspaper engine (../newspaper/newspaper.py): same Pango/Cairo text
layout, the same multiply-blend compositing so the paper texture shows through the
ink, the same resolution-independent zones (every position is a fraction of the
template dimensions, so one config works at any DPI), the same fontconfig font
registration, sRGB ICC tagging and print-production flags.

The template carries only the cream paper and the warm dark-brown double-rule
border. Everything inside it (title, subtitle, the crossword grid, the separator
rule, the Across/Down clue columns and the Sentrada credit) is drawn fresh each
run. The grid itself is generated by grid_generator.py from the candidate pool,
so its shape changes with every company.
"""


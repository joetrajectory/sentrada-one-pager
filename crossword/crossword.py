def render_title(cr, ctx, data):
    """Title in bold premium serif, centred, auto-sized to span the available
    width (and capped on height), then the subtitle centred beneath it."""
    W, H, cfg, ink = ctx["W"], ctx["H"], ctx["cfg"], ctx["cfg"]["ink"]
    company = (data.get("company_name") or "").strip()
    title = smarten(data.get("title") or f"THE {company} CROSSWORD").upper()

    avail_w = cfg["title_w_frac"] * W
    cap_h = cfg["title_h_max"] * H
    layout = make_layout(cr)

    def ink_extents(size):
        set_font(layout, cfg["font_title"], size)
        layout.set_attributes(build_attrs(title, letter_spacing_px=cfg["title_tracking"] * size))
        layout.set_text(title, -1)
        return layout.get_pixel_extents()[0]

    def feasible(size):
        r = ink_extents(size)
        return r.width <= avail_w and r.height <= cap_h

    size, fits = largest_fitting(feasible, 0.020 * H, 0.075 * H)
    r = ink_extents(size)
    ctx["title_fit"] = {"fits": bool(fits) and r.width <= avail_w,
                        "over_px": max(0.0, r.width - avail_w)}
    title_top = cfg["title_top"] * H
    # Centre on the page; offset by the ink rect so the caps sit optically centred.
    x = (W - r.width) / 2.0 - r.x
    y = title_top - r.y
    draw_layout(cr, layout, x, y, ink)
    title_bottom = title_top + r.height

    # Subtitle, centred beneath the title, FIT to the content width the same way
    # the title fits to width: try the configured size, wrap to at most
    # subtitle_max_lines, and shrink the font until it fits. A constrained,
    # centre-aligned layout (with char-level wrap as a last resort) means a long
    # subtitle can never shear off the page margins the way an unconstrained
    # single line did. The pre-render check (run_checks) FAILs if it still cannot
    # fit at the minimum size, rather than shipping a clipped piece.
    subtitle = smarten(data.get("subtitle") or "How well do you know your own company?")
    content_w = (1.0 - 2.0 * cfg["margin_x"]) * W
    max_lines = cfg["subtitle_max_lines"]
    sl = make_layout(cr)
    sl.set_width(int(round(content_w * SCALE)))
    sl.set_alignment(Pango.Alignment.CENTER)
    sl.set_wrap(Pango.WrapMode.WORD_CHAR)   # break a word only as a last resort; never clip

    def lay_subtitle(size):
        set_font(sl, cfg["font_subtitle"], size)
        sl.set_text(subtitle, -1)
        return sl.get_line_count(), max_line_width(sl)

    def fits_at(size):
        lines, widest = lay_subtitle(size)
        return lines <= max_lines and widest <= content_w + 1.0

    size, _ = largest_fitting(fits_at, cfg["size_subtitle_min"] * H, cfg["size_subtitle"] * H)
    n_lines, widest = lay_subtitle(size)        # re-lay at the chosen size
    sw, sh = measure(sl)
    sy = title_bottom + cfg["title_subtitle_gap"] * H
    # The box is content_w wide with centre alignment, so draw at the content-box
    # left edge and let Pango centre each line within it.
    draw_layout(cr, sl, (W - content_w) / 2.0, sy, ink)
    header_bottom = sy + sh
    ctx["header_bottom"] = header_bottom

    # Hand the fit result to run_checks: too long to fit in max_lines at the
    # minimum size, or a wrapped subtitle that intrudes into the grid reserve.
    grid_top = cfg["grid_top"] * H
    ctx["subtitle_fit"] = {
        "fits": n_lines <= max_lines and widest <= content_w + 1.0
                and header_bottom <= grid_top + 1.0,
        "lines": n_lines,
        "max_lines": max_lines,
        "overflow_px": max(0.0, widest - content_w),
        "collides_grid": header_bottom > grid_top + 1.0,
        # Fit succeeded but deviated from the configured design: surfaced as a
        # WARN so the operator knows the piece wrapped or shrank without having
        # to spot it in the render.
        "shrunk_to": size / H if size < cfg["size_subtitle"] * H - 0.5 else None,
    }


def compute_grid_geometry(ctx, gridres):
    """Square-cell geometry for the generated grid, centred horizontally on the
    page and within the reserved vertical band. Returns (cell, ox, oy)."""
    W, H, cfg = ctx["W"], ctx["H"], ctx["cfg"]
    rows, cols = gridres["rows"], gridres["cols"]
    zone_top = cfg["grid_top"] * H
    zone_h = cfg["grid_bottom"] * H - zone_top
    zone_w = cfg["grid_w_frac"] * W
    cell = min(zone_w / max(1, cols), zone_h / max(1, rows))
    grid_w, grid_h = cell * cols, cell * rows
    ox = (W - grid_w) / 2.0                       # centred on the page width
    oy = zone_top + (zone_h - grid_h) / 2.0       # centred in the reserved band
    return cell, ox, oy


def render_grid(grid_cr, ctx, gridres):
    """Draw the British-style grid on its OWN surface: warm-white cell fills with
    near-black borders, and small bold numbers in the top-left of each light's
    start cell. Only lettered cells are drawn; the paper shows through the gaps.
    Drawn on a separate surface because the white fills must sit ON TOP of the
    paper (the ink text is multiplied INTO the paper), not be multiplied away."""
    cfg = ctx["cfg"]
    rows, cols, grid = gridres["rows"], gridres["cols"], gridres["grid"]
    numbers = gridres["numbers"]
    cell, ox, oy = ctx["cell"], ctx["grid_ox"], ctx["grid_oy"]
    border = max(1.0, cfg["grid_border_frac"] * cell)

    # 1) White cell fills.
    grid_cr.set_source_rgba(*cfg["cell_fill"], 1.0)
    for r in range(rows):
        for c in range(cols):
            if grid[r][c] is not None:
                grid_cr.rectangle(ox + c * cell, oy + r * cell, cell, cell)
                grid_cr.fill()

    # 2) Cell borders. Drawn as a set of UNIQUE edges (in grid-line coordinates)
    #    so an edge shared by two filled cells is struck exactly once. Stroking
    #    each cell rectangle instead double-strikes interior edges, making them
    #    read bolder than the outer ones.
    grid_cr.set_source_rgba(*cfg["grid_border"], 1.0)
    grid_cr.set_line_width(border)
    grid_cr.set_line_cap(cairo.LINE_CAP_SQUARE)
    edges = set()
    for r in range(rows):
        for c in range(cols):
            if grid[r][c] is None:
                continue
            edges.add(((c, r), (c + 1, r)))            # top
            edges.add(((c, r + 1), (c + 1, r + 1)))    # bottom
            edges.add(((c, r), (c, r + 1)))            # left
            edges.add(((c + 1, r), (c + 1, r + 1)))    # right
    for (a, b) in edges:
        grid_cr.move_to(ox + a[0] * cell, oy + a[1] * cell)
        grid_cr.line_to(ox + b[0] * cell, oy + b[1] * cell)
        grid_cr.stroke()

    # 3) Clue numbers, small and bold, inset into each start cell's top-left.
    pad = cfg["grid_num_pad_frac"] * cell
    num_size = cfg["grid_num_frac"] * cell
    for key, num in numbers.items():
        r, c = (int(v) for v in key.split(","))
        nl = make_layout(grid_cr)
        set_font(nl, cfg["font_grid_num"], num_size)
        nl.set_text(str(num), -1)
        draw_layout(grid_cr, nl, ox + c * cell + pad, oy + r * cell + pad * 0.4,
                    cfg["grid_border"])

    ctx["grid_bottom_px"] = oy + rows * cell


def render_clues(cr, ctx, gridres):
    """Two clue columns below the separator rule: ACROSS (left) and DOWN (right),
    headers in spaced caps, numbers bold and text regular weight, all auto-sized
    so the taller column fits the available depth. Returns nothing; draws the
    separator rule position into ctx for the post-composite pass."""
    W, H, cfg, ink = ctx["W"], ctx["H"], ctx["cfg"], ctx["cfg"]["ink"]
    mx, rx = cfg["margin_x"] * W, cfg["right_x"] * W

    # Separator rule sits a set gap below the grid, spanning the content width.
    sep_y = ctx["grid_bottom_px"] + cfg["grid_sep_gap"] * H
    ctx["separator"] = (mx, rx, sep_y)

    head_y = sep_y + cfg["sep_clue_gap"] * H
    clue_bottom = cfg["clue_bottom"] * H

    col_gap = cfg["col_gap"] * W
    col_w = (rx - mx - col_gap) / 2.0
    col_x = [mx, mx + col_w + col_gap]
    across = [smarten_entry(e) for e in gridres["across"]]
    down = [smarten_entry(e) for e in gridres["down"]]

    # Header layouts (ACROSS / DOWN) in spaced caps; fixed size relative to the
    # clue size band so they read as section heads.
    head_size = cfg["size_clue_head"] * H

    def header_layout(text):
        hl = make_layout(cr)
        set_font(hl, cfg["font_clue_head"], head_size)
        hl.set_attributes(build_attrs(text.upper(),
                                      letter_spacing_px=cfg["clue_head_tracking"] * head_size))
        hl.set_text(text.upper(), -1)
        return hl

    ah, dh = header_layout("ACROSS"), header_layout("DOWN")
    head_h = max(measure(ah)[1], measure(dh)[1])
    clue_top = head_y + head_h + cfg["clue_head_gap"] * H
    avail_h = clue_bottom - clue_top

    def number_layout(num, size):
        nl = make_layout(cr)
        set_font(nl, cfg["font_clue_num"], size)
        nl.set_attributes(build_attrs(str(num), leading_px=cfg["clue_lead"] * size))
        nl.set_text(str(num), -1)
        return nl

    def text_layout(clue, size, text_w):
        tl = make_layout(cr)
        set_font(tl, cfg["font_clue_text"], size)
        tl.set_width(int(text_w * SCALE))
        tl.set_wrap(Pango.WrapMode.WORD)
        tl.set_attributes(build_attrs(clue, leading_px=cfg["clue_lead"] * size))
        tl.set_text(clue, -1)
        return tl

    def hang_for(entries, size):
        # Hanging indent = widest number in the column + a gap, so every clue's
        # text aligns to one edge regardless of one- vs two-digit numbers.
        widest = max(measure(number_layout(e["number"], size))[0] for e in entries) if entries else 0
        return widest + cfg["clue_num_gap_frac"] * size

    def column_height(entries, size):
        if not entries:
            return 0.0
        hang = hang_for(entries, size)
        text_w = col_w - hang
        gap = cfg["clue_gap_frac"] * size
        total = sum(measure(text_layout(e["clue"], size, text_w))[1] + gap for e in entries)
        return max(0.0, total - gap)

    def feasible(size):
        return (column_height(across, size) <= avail_h
                and column_height(down, size) <= avail_h)

    size, fits = largest_fitting(feasible, cfg["clue_size_min"] * H, cfg["clue_size_max"] * H)
    ctx["clues_fit"] = fits

    # Draw headers, then the clues beneath each: bold number at the column edge,
    # regular text hung to a common indent (top-aligned, shared leading).
    draw_layout(cr, ah, col_x[0], head_y, ink)
    draw_layout(cr, dh, col_x[1], head_y, ink)

    for ci, entries in ((0, across), (1, down)):
        if not entries:
            ctx[f"clue_foot_{ci}"] = clue_top
            continue
        hang = hang_for(entries, size)
        text_w = col_w - hang
        gap = cfg["clue_gap_frac"] * size
        y = clue_top
        for e in entries:
            tl = text_layout(e["clue"], size, text_w)
            nl = number_layout(e["number"], size)
            draw_layout(cr, nl, col_x[ci], y, ink)
            draw_layout(cr, tl, col_x[ci] + hang, y, ink)
            y += measure(tl)[1] + gap
        ctx[f"clue_foot_{ci}"] = y - gap


def smarten_entry(entry):
    e = dict(entry)
    e["clue"] = smarten(entry["clue"])
    return e


def render_credit(cr, ctx, data):
    """Lowercase 'sentrada' credit, small and discreet, in the bottom-right
    corner just inside the content margin."""
    W, H, cfg, ink = ctx["W"], ctx["H"], ctx["cfg"], ctx["cfg"]["ink"]
    text = cfg["credit_text"]
    cl = make_layout(cr)
    set_font(cl, cfg["font_credit"], cfg["size_credit"] * H)
    cl.set_text(text, -1)
    cw, ch = measure(cl)
    x = cfg["right_x"] * W - cw
    y = cfg["credit_bottom"] * H - ch     # anchor the BOTTOM so it clears the border
    draw_layout(cr, cl, x, y, ink)



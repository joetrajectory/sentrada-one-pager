def build(template_path, data_path, output_path, cfg=CONFIG,
          print_dpi=None, print_size=None, render_dpi=None, render_size=None,
          ink_blur=None, bleed_mm=0.0, crop_marks=False, seed=None):
    with open(data_path, "r", encoding="utf-8") as fh:
        data = json.load(fh)

    # Generate the grid (or accept a pre-built one in the data file).
    if data.get("grid_data"):
        gridres = data["grid_data"]
    else:
        eff_seed = seed if seed is not None else data.get("seed")
        gridres = grid_generator.generate_grid(
            data["candidates"],
            min_words=data.get("min_words", cfg["min_words"]),
            max_words=data.get("max_words", cfg["max_words"]),
            seed=eff_seed, attempts=cfg["grid_attempts"],
            anchors=data.get("anchors"))
    print(f"[info] grid: {gridres['word_count']} words on {gridres['rows']}x{gridres['cols']} "
          f"({len(gridres['across'])} across, {len(gridres['down'])} down)")
    if gridres.get("anchors"):
        placed_anchors = [a for a in gridres["anchors"] if a not in gridres["anchors_missing"]]
        print(f"[info] anchors: {len(placed_anchors)}/{len(gridres['anchors'])} placed"
              + (f"; MISSING: {', '.join(gridres['anchors_missing'])}" if gridres["anchors_missing"] else ""))

    template = Image.open(template_path).convert("RGB")
    print(f"[info] template {os.path.basename(template_path)}: {template.width}x{template.height}px")

    if render_size or render_dpi:
        if render_size:
            tw, th = render_size
        else:
            mm = cfg["a2_mm"]
            tw = int(round(mm[0] / 25.4 * render_dpi))
            th = int(round(mm[1] / 25.4 * render_dpi))
        if (tw, th) != template.size:
            print(f"[info] upscaling template {template.width}x{template.height} -> {tw}x{th} "
                  f"before rendering" + (f" ({render_dpi} DPI)" if render_dpi else ""))
            template = template.resize((tw, th), Image.LANCZOS)
    W, H = template.size

    # Two cairo surfaces: ink text (multiplied into paper) and the grid (white
    # cells composited on top of the paper).
    ink_surface = cairo.ImageSurface(cairo.FORMAT_ARGB32, W, H)
    ink_cr = cairo.Context(ink_surface)
    assert_fonts_resolved(ink_cr, cfg)
    grid_surface = cairo.ImageSurface(cairo.FORMAT_ARGB32, W, H)
    grid_cr = cairo.Context(grid_surface)

    ctx = {"W": W, "H": H, "cfg": cfg}
    cell, ox, oy = compute_grid_geometry(ctx, gridres)
    ctx["cell"], ctx["grid_ox"], ctx["grid_oy"] = cell, ox, oy
    print(f"[info] grid cell {cell:.1f}px ({cell/H:.4f}H), grid block "
          f"{cell*gridres['cols']:.0f}x{cell*gridres['rows']:.0f}px")

    render_title(ink_cr, ctx, data)
    render_grid(grid_cr, ctx, gridres)
    render_clues(ink_cr, ctx, gridres)
    render_credit(ink_cr, ctx, data)

    ink_rgba = cairo_to_pil(ink_surface)
    result = composite_ink(template, ink_rgba, cfg, W, ink_blur=ink_blur)
    result = draw_separator(result, ctx)
    # Composite the grid (warm-white cells, dark borders, numbers) over the paper.
    grid_rgba = cairo_to_pil(grid_surface)
    result = result.convert("RGBA")
    result.alpha_composite(grid_rgba)
    result = result.convert("RGB")

    result, dpi = finalize_output(result, cfg, print_dpi, print_size)
    if bleed_mm > 0 or crop_marks:
        result = add_bleed_and_marks(result, cfg, bleed_mm, crop_marks)

    diagnostics = run_checks(ctx, gridres, cfg)
    fails = [d for d in diagnostics if d[0] == "fail"]
    if output_path is not None:
        save_kwargs = {"icc_profile": srgb_icc_bytes()}
        if dpi:
            save_kwargs["dpi"] = (dpi, dpi)
        if fails:
            root, ext = os.path.splitext(output_path)
            quarantine = root + ".FAILED" + ext
            result.save(quarantine, **save_kwargs)
            print(f"[error] {len(fails)} layout failure(s); deliverable WITHHELD:")
            for _lvl, field, msg in fails:
                print(f"[error]   {field}: {msg}")
            print(f"[error] wrote quarantined render to {quarantine} (NOT print-ready).")
        else:
            result.save(output_path, **save_kwargs)
            print(f"[done] wrote {output_path} ({result.width}x{result.height}px)")
    return diagnostics


def main():
    ap = argparse.ArgumentParser(description="Render a company crossword onto a blank template.")
    ap.add_argument("--template", required=True)
    ap.add_argument("--data", required=True)
    ap.add_argument("--output", default=None, help="output PNG path (optional with --check)")
    ap.add_argument("--check", action="store_true", help="validate and print a PASS/FAIL report")
    ap.add_argument("--print-dpi", type=int, default=None,
                    help="downsample the finished page to A2 at this DPI and tag the PNG")
    ap.add_argument("--print-size", default=None, help="explicit WIDTHxHEIGHT, overrides --print-dpi")
    ap.add_argument("--render-dpi", type=int, default=None,
                    help="upscale the template to A2 at this DPI BEFORE rendering")
    ap.add_argument("--render-size", default=None, help="explicit WIDTHxHEIGHT to upscale to")
    ap.add_argument("--seed", type=int, default=None, help="grid generation seed (reproducible)")
    ap.add_argument("--ink-blur", type=float, default=None,
                    help="ink-spread blur in px on the text (0 = razor sharp)")
    ap.add_argument("--bleed-mm", type=float, default=0.0, help="bleed (mm) on every side")
    ap.add_argument("--crop-marks", action="store_true", help="draw crop marks in the bleed margin")
    args = ap.parse_args()

    def parse_size(s):
        if not s:
            return None
        w, h = s.lower().split("x")
        return (int(w), int(h))

    if not args.check and not args.output:
        ap.error("--output is required unless --check is given")

    render_dpi = args.render_dpi
    render_size = parse_size(args.render_size)
    # No render-dpi default for --check: the gate lays out at the native
    # template raster, exactly as the production render does (see the
    # newspaper engine for the rationale).

    diagnostics = build(
        args.template, args.data, args.output,
        print_dpi=args.print_dpi, print_size=parse_size(args.print_size),
        render_dpi=render_dpi, render_size=render_size, ink_blur=args.ink_blur,
        bleed_mm=args.bleed_mm, crop_marks=args.crop_marks, seed=args.seed)

    if args.check:
        ok = print_report(diagnostics, os.path.basename(args.data))
        sys.exit(0 if ok else 1)
    if any(d[0] == "fail" for d in diagnostics):
        sys.exit(1)


if __name__ == "__main__":
    main()
